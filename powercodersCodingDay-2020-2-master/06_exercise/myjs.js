var days=prompt("Enter an integer between 1 and 5");
 
  switch(days)
  {
     case "1":
        alert("Monday");
     break;

     case "2":
      alert("Tuesday");
     break;

     case "3":
      alert("Wednesday");
     break;

     case "4":
      alert("Thursday");
     break;

     case "5":
      alert("Friday");
     break;

     default:
      alert("Please, enter an integer between 1 and 5");
     break;
  }